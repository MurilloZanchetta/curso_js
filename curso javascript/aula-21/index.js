function soma (x =1, y =1) {
    const resultado = x + y;
    return resultado
}


console.log(soma(50, 3))
console.log(soma(90, 800))
console.log(soma(250, 570))
console.log(soma(12, 3))
console.log(soma(33, 43))
console.log(soma(45, 5))
console.log(soma(44, 4444))
console.log(soma())
console.log(soma(4))


